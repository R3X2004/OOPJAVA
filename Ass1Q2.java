import java.lang.*;
	class Ass1Q2{
		public static void main (String[] args) {
			int a=10;
			int b=3;
			System.out.println("a=10, b=3");
			System.out.println("Arithmetic operators:");
			System.out.println("a + b = " + (a + b));
			System.out.println("a - b = " + (a - b));
			System.out.println("a * b = " + (a * b));
			System.out.println("a / b = " + (a / b));
			System.out.println("a % b = " + (a % b));
			
			System.out.println("Bitwise operators:");
			System.out.println("a & b = " + (a & b)); 
			System.out.println("a | b = " + (a | b)); 
			System.out.println("a ^ b = " + (a ^ b));
			System.out.println("a ^ b = " + (a ^ b));
			System.out.println("a << 1 = " + (a << 1));
			System.out.println("a >> 1 = " + (a >> 1));
		}
	}
			
			
        